def build_spanish_prompts(n: int, seed: int) -> list[str]:
    """n realistic Spanish prompts, deterministic per seed (template choice
    and every slot come from one random.Random(seed))."""
    rng = random.Random(seed)
    out = []
    for _ in range(n):
        tpl, slots = SPANISH_TEMPLATES[rng.randrange(len(SPANISH_TEMPLATES))]
        out.append(tpl.format(**{k: rng.choice(v) for k, v in slots.items()}))
    return out


# ---------------------------------------------------------------------------
# CodeAlpaca prompts (60%)
# ---------------------------------------------------------------------------

def ensure_code_alpaca(cache_dir: Path = RAW_DIR) -> list[str]:
    """Download CodeAlpaca-20k raw JSON once (cache in cache_dir), return the
    prompt list: 'instruction' plus 'input' appended when non-empty. The
    dataset's own 'output' answers are deliberately IGNORED (DFlash rule)."""
    cache_dir.mkdir(parents=True, exist_ok=True)
    path = cache_dir / "code_alpaca_20k.json"
    if not path.exists():
        print("[gen_dataset] descargando CodeAlpaca-20k -> %s" % path,
              flush=True)
        with urllib.request.urlopen(CODE_ALPACA_URL, timeout=120) as r:
            data = r.read()
        tmp = path.with_suffix(".tmp")
        tmp.write_bytes(data)
        tmp.replace(path)
    rows = json.loads(path.read_text(encoding="utf-8"))
    prompts = []
    for row in rows:
        ins = (row.get("instruction") or "").strip()
        extra = (row.get("input") or "").strip()
        if not ins:
            continue
        prompts.append(ins + ("\n" + extra if extra else ""))
    return prompts


def build_prompt_plan(n: int, seed: int, code_prompts: list[str]) -> list[str]:
    """Deterministic plan of n prompts: position i is code iff i % 5 < 3
    (exactly 60% code / 40% Spanish). Code prompts are seed-shuffled and
    cycled if n*0.6 exceeds the pool. Pure function of (n, seed,
    code_prompts) — the resume logic depends on this determinism."""
    code = list(code_prompts)
    random.Random(seed).shuffle(code)
    n_code = sum(1 for i in range(n) if i % 5 < 3)
    spanish = build_spanish_prompts(n - n_code, seed + 1)
    plan, ci, si = [], 0, 0
    for i in range(n):
        if i % 5 < 3:
            plan.append(code[ci % len(code)])
            ci += 1
        else:
            plan.append(spanish[si])
            si += 1
    return plan


# ---------------------------------------------------------------------------
# Regeneration against llama-server (:8088)
# ---------------------------------------------------------------------------

def regenerate(prompt: str, server: str = SERVER_URL, timeout: float = 300.0,
               retries: int = 3) -> str:
    """One prompt -> target answer via llama-server /completion (ChatML,
    T=0.7, n_predict 320, stop <|im_end|>). Retries with exponential backoff;
    returns '' after the last failure (caller counts it as a skip)."""
    payload = json.dumps({
        "prompt": _apply_qwen_template(prompt),
        "temperature": 0.7,
        "n_predict": 320,
        "stop": ["<|im_end|>"],
    }).encode("utf-8")
    for attempt in range(retries + 1):
        try:
            req = urllib.request.Request(
                server, data=payload,
                headers={"Content-Type": "application/json"})
            with urllib.request.urlopen(req, timeout=timeout) as r:
                body = json.loads(r.read().decode("utf-8"))
            return (body.get("content") or "").strip()
        except (urllib.error.URLError, TimeoutError, OSError, ValueError):
            if attempt == retries:
                return ""
            time.sleep(2.0 ** attempt)
    return ""


# ---------------------------------------------------------------------------
# Resume + main loop
# ---------------------------------------------------------------------------

def resume_point(out_path) -> int:
    """Non-empty line count of the output JSONL == next plan index."""
    p = Path(out_path)
    if not p.exists():
        return 0
    with open(p, "rb") as f:
        return sum(1 for line in f if line.strip())


def main(argv=None):
    ap = argparse.ArgumentParser(
        description="Cognia-BDraft v0 dataset: prompts -> target-regenerated "
                    "answers (llama-server :8088)")
    ap.add_argument("--n", type=int, default=12000)
    ap.add_argument("--workers", type=int, default=4)
    ap.add_argument("--seed", type=int, default=0)
    ap.add_argument("--out", default=str(DATA_DIR / "v0.jsonl"))
    ap.add_argument("--server", default=SERVER_URL)
    args = ap.parse_args(argv)

    out = Path(args.out)
    out.parent.mkdir(parents=True, exist_ok=True)
    plan = build_prompt_plan(args.n, args.seed, ensure_code_alpaca())
    done = resume_point(out)
    if done >= args.n:
        print("[gen_dataset] %s ya tiene %d/%d lineas, nada que hacer"
              % (out, done, args.n), flush=True)
        _write_done(out, args, done, 0, 0, 0.0)
        return
    print("[gen_dataset] %d/%d ya en %s; generando %d prompts con %d workers"
          % (done, args.n, out, args.n - done, args.workers), flush=True)

    t0 = time.time()
    written = skipped = consec_fail = 0
    idx = done
    pending = plan[done:]
    chunk_size = max(1, args.workers) * 8
    with open(out, "a", encoding="utf-8") as f:
        for start in range(0, len(pending), chunk_size):
            chunk = pending[start:start + chunk_size]
            with ThreadPoolExecutor(max_workers=args.workers) as ex:
                results = list(ex.map(
                    lambda p: regenerate(p, server=args.server), chunk))
            for prompt, resp in zip(chunk, results):
                idx += 1
                if len(resp) < MIN_ANSWER_CHARS:
                    skipped += 1
                    consec_fail += 1
                else:
                    f.write(json.dumps({"prompt": prompt, "respuesta": resp},
                                       ensure_ascii=False) + "\n")
                    f.flush()
                    written += 1
                    consec_fail = 0
                if idx % 100 == 0:
                    rate = (written + skipped) / max(time.time() - t0, 1e-9)
                    print("[gen_dataset] %d/%d  escritas %d  skips %d  "
                          "%.2f prompts/s" % (idx, args.n, written, skipped,
                                              rate), flush=True)
            if consec_fail >= MAX_CONSEC_FAILS:
                raise SystemExit(
                    "[gen_dataset] %d respuestas vacias seguidas: el server "
                    "%s parece caido; reintentar cuando vuelva (reanuda solo)"
                    % (consec_fail, args.server))
    _write_done(out, args, resume_point(out), written, skipped,
                time.time() - t0)
    print("[gen_dataset] LISTO: %d lineas en %s (escritas %d, skips %d, "
          "%.1f s)" % (resume_point(out), out, written, skipped,
                       time.time() - t0), flush=True)


def _write_done(out: Path, args, total_lines: int, written: int, skipped: int,
                seconds: float):
    stats = {"n_plan": args.n, "seed": args.seed, "lineas": total_lines,
             "escritas_ultima_corrida": written, "skips": skipped,
             "segundos": round(seconds, 1),
             "fecha": time.strftime("%Y-%m-%d %H:%M:%S")}
    Path(str(out) + ".done").write_text(
        json.dumps(stats, ensure_ascii=False, indent=2), encoding="utf-8")


if __name__ == "__main__":
    main()
