"""
bdraft/gen_dataset.py
=====================
v0 dataset generator for Cognia-BDraft, per
planes/DSPARK_GEMMA_DRAFT_MODEL.md section 2.3.

Prompt sources (no `datasets` lib, raw download + local cache):
  - 60% code/EN: CodeAlpaca-20k raw JSON ('instruction' + 'input'), cached in
    ~/.cognia/bdraft_data/raw/;
  - 40% Spanish: 34 parametric templates (explanations, writing, everyday
    reasoning, basic math), deterministic per seed, realistic prompts.

DFlash rule (imprescindible): the answers are REGENERATED by the target
Qwen2.5-7B-Instruct (llama-server on :8088, ChatML, T=0.7) so the draft
learns the TARGET's distribution — the original dataset answers are never
used.

Output: JSONL {"prompt", "respuesta"} appended to ~/.cognia/bdraft_data/
v0.jsonl, plus a v0.jsonl.done sidecar with stats when the plan completes.
RESUMABLE: the prompt plan is a pure function of (seed, n); on restart the
existing lines are counted and generation continues from that index.
Answers under 20 chars (after retries) are skipped — not written — so after
a skip the resume index drifts back by one and a few prompts may be sampled
twice; harmless at T=0.7, and it keeps the resume rule dead simple
("line count == plan index").

No torch here: runs on any venv (venv312 is fine).
"""

